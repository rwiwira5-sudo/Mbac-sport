<?php
// 1. เริ่มต้น Session
// ต้องเรียกใช้ session_start() ก่อนเสมอเพื่อจัดการกับ session
session_start();

// 2. ล้างข้อมูลทั้งหมดในตัวแปร $_SESSION
// เป็นการลบข้อมูลผู้ใช้ที่เก็บไว้ เช่น user_id, fname, roll_id
$_SESSION = array();

// 3. ทำลาย Session ที่เซิร์ฟเวอร์
// คำสั่งนี้จะล้าง session ของผู้ใช้ออกจากเซิร์ฟเวอร์อย่างสมบูรณ์
session_destroy();

// 4. ส่งผู้ใช้กลับไปยังหน้าเข้าสู่ระบบ (login.php)
// หลังจากออกจากระบบแล้ว ผู้ใช้ควรจะกลับไปที่หน้าแรกหรือหน้าล็อกอิน
header("Location: login.php");

// 5. หยุดการทำงานของสคริปต์
// เป็นการปฏิบัติที่ดีที่ควรเรียก exit() หลังจากใช้ header() เพื่อให้แน่ใจว่าไม่มีโค้ดอื่นทำงานต่อ
exit();
?>